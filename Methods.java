public class Main {
	public static void add_num(int a, int b){
		System.out.println(a+b);
	}
	public static void sub_num(int a, int b){
		System.out.println(a-b);
	}
	public static void mul_num(int a, int b){
		System.out.println(a*b);
	}
	public static void divide_num(int a, int b){
		System.out.println(a/b);
	}
	
	public static void main(String[] args) {
		add_num(6,8);
		sub_num(6,8);
		mul_num(6,8);
		divide_num(6,8);
	}
}
